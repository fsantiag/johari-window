		It is necessary to improve self-awareness and personal development among individuals when they are in a group.
		The 'Johari' window model is a convenient method used to achieve this task of understanding and enhancing communication between the members in a group. 
		American psychologists Joseph Luft and Harry Ingham developed this model in 1955. 
		The idea was derived as the upshot of the group dynamics in University of California and was later improved by Joseph Luft. 
		The name 'Johari' came from joining their first two names. 
		This model is also denoted as feedback/disclosure model of self-awareness.
